// ─── Helper Functions ──────────────────────────────────────────────────────────
export function getDrillsByCategory(category) {
  return DRILLS.filter((d) => d.category === category);
}

export function getDrillsByFramework(framework) {
  return DRILLS.filter((d) => d.framework === framework);
}

export function searchDrills(query) {
  const q = query.toLowerCase();
  return DRILLS.filter(
    (d) =>
      d.name.toLowerCase().includes(q) ||
      d.description.toLowerCase().includes(q) ||
      d.category.toLowerCase().includes(q) ||
      d.framework.toLowerCase().includes(q)
  );
}

export function getDrillById(id) {
  return DRILLS.find((d) => d.id === id);
}
